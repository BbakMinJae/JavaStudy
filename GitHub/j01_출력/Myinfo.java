package j01_출력;

public class Myinfo {
	public static void main(String[] args) {
		String name; // 변수 선언
		name = "홍길동"; // 변수에 값 할당, 대입
		System.out.println(name); // 출력
		
		// String name; 변수 선언은 무조건 한번만!!!
		name = "박민재";
		System.out.println(name);
		
		int age = 18; // 변수 선언과 초기화
		System.out.println(age);
		// 자동완성 단축키 : ctrl + space
		// 실행 단축기 : ctrl + F11
	}
}
